package Entidad;

public class ClsEntidadCategoria {
    private String strIdCategoria;
    private String strDescripcionCategoria;
    private String strUltimoIdCategoria;
    private String strSiguienteCodigo;
    private String strSoloCodigo;
        
    public String getStrIdCategoria() {
        return strIdCategoria;
    }

    public void setStrIdCategoria(String strIdCategoria) {
        this.strIdCategoria = strIdCategoria;
    }

    public String getStrDescripcionCategoria() {
        return strDescripcionCategoria;
    }

    public void setStrDescripcionCategoria(String strDescripcionCategoria) {
        this.strDescripcionCategoria = strDescripcionCategoria;
    }

    public String getStrUltimoIdCategoria() {
        return strUltimoIdCategoria;
    }

    public void setStrUltimoIdCategoria(String strUltimoIdCategoria) {
        this.strUltimoIdCategoria = strUltimoIdCategoria;
    }

    public String getStrSoloCodigo() {
        return strSoloCodigo;
    }

    public void setStrSoloCodigo(String strSoloCodigo) {
        this.strSoloCodigo = strSoloCodigo;
    }

    public String getStrSiguienteCodigo() {
        return strSiguienteCodigo;
    }

    public void setStrSiguienteCodigo(String strSiguienteCodigo) {
        this.strSiguienteCodigo = strSiguienteCodigo;
    }
}
