public class ClsEntidadServicio {
    
    public int IntIdServicio;
    public String StrNombreServicio;
    public double DoublePrecioServicio;
    public String StrDescripcionServicio;

    public int getIntIdServicio() {
        return IntIdServicio;
    }

    public void setIntIdServicio(int IntIdServicio) {
        this.IntIdServicio = IntIdServicio;
    }

    public String getStrNombreServicio() {
        return StrNombreServicio;
    }

    public void setStrNombreServicio(String StrNombreServicio) {
        this.StrNombreServicio = StrNombreServicio;
    }

    public double getDoublePrecioServicio() {
        return DoublePrecioServicio;
    }

    public void setDoublePrecioServicio(double DoublePrecioServicio) {
        this.DoublePrecioServicio = DoublePrecioServicio;
    }

    public String getStrDescripcionServicio() {
        return StrDescripcionServicio;
    }

    public void setStrDescripcionServicio(String StrDescripcionServicio) {
        this.StrDescripcionServicio = StrDescripcionServicio;
    }
}
