public class ClsEntidadGrupo {
    private String StrIdGrupo;
    private String StrNombre;
    private String StrCantidad;
    private Double strDescuento;

    public String getStrIdGrupo() {
        return StrIdGrupo;
    }

    public void setStrIdGrupo(String StrIdGrupo) {
        this.StrIdGrupo = StrIdGrupo;
    }

    public String getStrNombre() {
        return StrNombre;
    }

    public void setStrNombre(String StrNombre) {
        this.StrNombre = StrNombre;
    }

    public String getStrCantidad() {
        return StrCantidad;
    }

    public void setStrCantidad(String StrCantidad) {
        this.StrCantidad = StrCantidad;
    }

    public Double getStrDescuento() {
        return strDescuento;
    }

    public void setStrDescuento(Double strDescuento) {
        this.strDescuento = strDescuento;
    } 
}
