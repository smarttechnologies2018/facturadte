public class ClsEntidadBanco {

    private String strIdBanco;
    private String strNombreBanco;
    private String strDireccion;
    private String strTelefono;
    private String strEmail;

    public String getStrIdBanco() {
        return strIdBanco;
    }

    public void setStrIdBanco(String strIdBanco) {
        this.strIdBanco = strIdBanco;
    }

    public String getStrNombreBanco() {
        return strNombreBanco;
    }

    public void setStrNombreBanco(String strNombreBanco) {
        this.strNombreBanco = strNombreBanco;
    }

    public String getStrDireccion() {
        return strDireccion;
    }

    public void setStrDireccion(String strDireccion) {
        this.strDireccion = strDireccion;
    }

    public String getStrTelefono() {
        return strTelefono;
    }

    public void setStrTelefono(String strTelefono) {
        this.strTelefono = strTelefono;
    }

    public String getStrEmail() {
        return strEmail;
    }

    public void setStrEmail(String strEmail) {
        this.strEmail = strEmail;
    }
}
